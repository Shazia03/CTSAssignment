package com.cts.java.sample;

public class Number
{

		public int sum(int a,int b)
		{
			int c=a+b;
			return c;
		}
		
		public int big(int x,int y)
		{
			int z=(x>y?x:y);
			return z;
		}
		
		public boolean evenodd(int d)
		{
			if(d%2==0)
			return true;
			else
			return false;	
		}
		

		
  }
